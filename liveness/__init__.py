'''
Liveness UI framework
=====================

Liveness UI is an open source library for developing distributed applications for
musical and artistical proupose. It is cross-platform (Linux/OSX/Windows/Android/iOS)
and released under the terms of the `MIT License <https://en.wikipedia.org/wiki/MIT_License>`_.

This library is based on Kivy, a python framework coming with native support
for many multi-touch input devices, a growing library of multi-touch aware widgets
and hardware accelerated OpenGL drawing.

Liveness is designed to decorate this widgets with multi-threading and network
functionalities, this focus on building custom and highly interactive applications
distribuited into many devices, and able to communicate with differents computer
music languages such as Supercollider, Chuck, Impromptu, PureData.

The point is create live interfaces, that could be created and changed in remote
from others devices, and aslo that could be send datas to every process in every
part of the network.
'''
